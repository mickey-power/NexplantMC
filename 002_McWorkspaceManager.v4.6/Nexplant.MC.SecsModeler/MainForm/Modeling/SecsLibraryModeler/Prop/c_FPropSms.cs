                        FDebug.throwFException(fUIWizard.generateMessage("E0001", new object[] { "Value" }));
                    }

                    if (value == 0)
                    {
                        if (m_fSms.hasChildSecondarySecsMessage)
                        {
                            FDebug.throwFException(fUIWizard.generateMessage("E0017", new object[] { "SECS Messages", "SECS Message" }));
                        }
                    }
                    else if (value % 2 == 0)
                    {
                        FDebug.throwFException(fUIWizard.generateMessage("E0018", new object[] { "SECS Messages" }));
                    }

                    // --

                    m_fSms.function = value;
                }
                catch (Exception ex)
                {
                    FMessageBox.showError(FConstants.ApplicationName, ex, this.mainObject.fWsmCore.fWsmContainer);
                }
                finally
                {

                }
