        public void removeAllChildDataSet(
            )
        {
            FDataSetCollection fDatCollection = null;

            try
            {
                fDatCollection = this.fChildDataSetCollection;
                if (fDatCollection.count == 0)
                {
                    return;
                }

                // --

                foreach (FDataSet fDat in fDatCollection)
                {
                    if (fDat.locked)
                    {
                        FDebug.throwFException(string.Format(FConstants.err_m_0012, "Object"));
                    }
                }

                // --

                foreach (FDataSet fDat in fDatCollection)
                {
                    fDat.remove();
                }
            }
            catch (Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {
                if (fDatCollection != null)
                {
                    fDatCollection.Dispose();
                    fDatCollection = null;
                }
            }
        }

