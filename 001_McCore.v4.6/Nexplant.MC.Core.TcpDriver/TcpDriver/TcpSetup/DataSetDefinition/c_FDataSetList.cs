                }

                // --

                foreach (FDataSet fDat in fChilds)
                {
                    FTcpDriverCommon.validateRemoveChildObject(this.fXmlNode, fDat.fXmlNode);
                }

                // --

                foreach (FDataSet fDat in fChilds)
                {
                    fDat.remove();
                }
            }
            catch (Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {

            }
        }
