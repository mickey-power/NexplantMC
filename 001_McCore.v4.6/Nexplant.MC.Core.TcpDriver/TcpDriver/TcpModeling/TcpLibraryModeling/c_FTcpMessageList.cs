        internal void lockObject(
            )
        {
            try
            {
                if (this.locked)
                {
                    return;
                }

                // --

                // ***
                // TCP Message List에 대한 Lock 처리
                // ***
                this.fXmlNode.set_attrVal(FXmlTagTML.A_Locked, FXmlTagTML.D_Locked, FBoolean.True, true);
            }
            catch (Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {

            }
        }

        //------------------------------------------------------------------------------------------------------------------------

        internal void unlockObject(
            )
        {
            string xpath = string.Empty;

            try
            {
                if (!this.locked)
                {
                    return;
                }

                // --

                // ***
                // Lock되어 있는 자식 TCP Messages가 존재할 경우 Unlock 작업을 취소한다.
                // ***
                xpath = FXmlTagTMS.E_TcpMessages + "[@" + FXmlTagTMS.A_Locked + "='" + FBoolean.True + "']";
                // --
                if (this.fXmlNode.containsNode(xpath))
                {
                    return;
                }

                // --

                this.fXmlNode.set_attrVal(FXmlTagTML.A_Locked, FXmlTagTML.D_Locked, FBoolean.False, true);
            }
            catch (Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {

            }
        }

