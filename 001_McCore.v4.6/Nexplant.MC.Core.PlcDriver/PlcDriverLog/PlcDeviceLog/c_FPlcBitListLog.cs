        public FIObjectLog fParent
        {
            get
            {
                string logType = string.Empty;

                try
                {
                    if (this.fXmlNode.fParentNode == null)
                    {
                        return null;
                    }

                    // --

                    logType = this.fXmlNode.fParentNode.get_attrVal(FXmlTagPMGL.A_LogType, FXmlTagPMGL.D_LogType);
                    if (logType == FXmlTagPMGL.L_Read)
                    {
                        return new FPlcDeviceDataMessageReadLog(this.fPcdlCore, this.fXmlNode.fParentNode);
                    }
                    else if (logType == FXmlTagPMGL.L_Written)
                    {
                        return new FPlcDeviceDataMessageWrittenLog(this.fPcdlCore, this.fXmlNode.fParentNode);
                    }                   
                }
                catch (Exception ex)
