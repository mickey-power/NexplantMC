        public string ToString(
            FStringOption option
            )
        {
            string info = string.Empty;
            string logType = string.Empty;

            try
            {
                if (option == FStringOption.Default)
                {
                    info = this.name;
                }
                else
                {
                    logType = (fObjectLogType == FObjectLogType.SecsDeviceBlockReceivedLog) ? "BLOCK RECV" : "BLOCK SENT";

                    // -- 

                    info = "[" + this.time + "] " + this.name + " [S" + stream + " F" + function + "] BlockNo=[" + blockNo + "] Length=[" + length + "]";
                    // --
                    if (this.resultMessage != string.Empty)
                    {
                        info += (" Msg=[" + this.resultMessage + "]");
                    }
                }

                // --

                if (this.description != string.Empty)
                {
                    info += (" Desc=[" + this.description + "]");
                }

                // -- 

                return info;
            }
            catch (Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {

            }
            return string.Empty;
