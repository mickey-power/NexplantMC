        //------------------------------------------------------------------------------------------------------------------------

        public void removeChildEnvironment(
            FEnvironment[] fChilds
            )
        {
            try
            {
                if (fChilds.Length == 0)
                {
                    return;
                }

                // --

                foreach (FEnvironment fEvn in fChilds)
                {
                    FSecsDriverCommon.validateRemoveChildObject(this.fXmlNode, fEvn.fXmlNode);                   
                }

                // --

                foreach (FEnvironment fEv in fChilds)
                {
                    fEv.remove();
                }
            }
            catch (Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {

            }
        }

