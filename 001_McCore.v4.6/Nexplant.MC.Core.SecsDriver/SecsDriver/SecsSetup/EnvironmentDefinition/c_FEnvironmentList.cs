                {
                    return;
                }

                // --

                foreach (FEnvironment FEnv in fEnvCollection)
                {
                    if (FEnv.locked)
                    {
                        FDebug.throwFException(string.Format(FConstants.err_m_0012, "Object"));
                    }
                }

                // --

                foreach (FEnvironment FEnv in fEnvCollection)
                {
                    FEnv.remove();
                }
            }
            catch(Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {
                if(fEnvCollection != null)
                {
                    fEnvCollection.Dispose();
                    fEnvCollection = null;
                }
            }
        }

