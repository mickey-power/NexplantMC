                    FDebug.throwFException(string.Format(FConstants.err_m_0011, "Reference Object", "Modeling Object"));
                }

                if (fRefObject.fChildEquipmentStateCollection.count > 0)
                {
                    if (this.Equals(fRefObject.fChildEquipmentStateCollection[fRefObject.fChildEquipmentStateCollection.count - 1]))
                    {
                        FDebug.throwFException(string.Format(FConstants.err_m_0060, "Object", "same localtion"));
