        public void removeChildSecsLibrary(
            FSecsLibrary[] fChilds
            )
        {
            try
            {
                if (fChilds.Length == 0)
                {
                    return;
                }

                // --

                foreach (FSecsLibrary fSlb in fChilds)
                {
                    FSecsDriverCommon.validateRemoveChildObject(this.fXmlNode, fSlb.fXmlNode);
                }

                // --

                foreach (FSecsLibrary fSlb in fChilds)
                {
                    fSlb.remove();
                }
            }
            catch (Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {

            }
        }

