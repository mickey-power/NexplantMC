        {
            FSecsLibraryCollection fSlbCollction = null;

            try
            {
                fSlbCollction = this.fChildSecsLibraryCollection;
                if (fSlbCollction.count == 0)
                {
                    return;
                }

                // --

                foreach (FSecsLibrary fSlb in fSlbCollction)
                {
                    if (fSlb.locked)
                    {
                        FDebug.throwFException(string.Format(FConstants.err_m_0012, "Object"));
                    }
                }

                // --

                foreach (FSecsLibrary fSlb in fSlbCollction)
                {
                    fSlb.remove();
                }                                
            }
            catch (Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {
                if (fSlbCollction != null)
                {
                    fSlbCollction.Dispose();
                    fSlbCollction = null;
                }
            }
        }

