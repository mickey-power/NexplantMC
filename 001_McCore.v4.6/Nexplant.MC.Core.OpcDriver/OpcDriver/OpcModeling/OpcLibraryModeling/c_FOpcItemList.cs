        internal void lockObject(
            )
        {
            try
            {
                if (this.locked)
                {
                    return;
                }

                // --

                // ***
                // OPC Item List에 대한 Lock 처리
                // ***
                this.fXmlNode.set_attrVal(FXmlTagOIL.A_Locked, FXmlTagOIL.D_Locked, FBoolean.True, true);
            }
            catch (Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {

            }
        }

        //------------------------------------------------------------------------------------------------------------------------

        internal void unlockObject(
            )
        {
            string xpath = string.Empty;

            try
            {
                if (!this.locked)
                {
                    return;
                }

                // --

                // ***
                // Lock되어 있는 자식 OPC Item이 존재할 경우 Unlock 작업을 취소한다.
                // ***
                xpath = FXmlTagOIT.E_OpcItem + "[@" + FXmlTagOIT.A_Locked + "='" + FBoolean.True + "']";
                if (this.fXmlNode.containsNode(xpath))
                {
                    return;
                }

                // --

                this.fXmlNode.set_attrVal(FXmlTagOIL.A_Locked, FXmlTagOIL.D_Locked, FBoolean.False, true);
            }
            catch (Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {

            }
        }

