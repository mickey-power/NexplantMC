        public FOpcItem pasteChild(
            )
        {
            FOpcItem fOit = null;
            FOpcEventItem fOei = null;

            try
            {
                if (FClipboard.containsData(FCbObjectFormat.OpcItem))
                {
                    FOpcDriverCommon.validatePasteChildObject(this.fXmlNode, FCbObjectFormat.OpcItem);

                    // --

                    fOit = (FOpcItem)this.pasteObject(FCbObjectFormat.OpcItem);
                }
                else
                {
                    FOpcDriverCommon.validatePasteChildObject(this.fXmlNode, FCbObjectFormat.OpcEventItem);
                    
                    // --
                    fOei = (FOpcEventItem)this.pasteObject(FCbObjectFormat.OpcEventItem);
                    fOit = changeEventItemToOpcItem(fOei);

                }
                return this.appendChildOpcItem(fOit);
            }
            catch (Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {
                fOit = null;
                fOei = null;
            }
            return null;
        }
