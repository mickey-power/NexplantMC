        {
            try
            {
                if (fChilds.Length == 0)
                {
                    return;
                }

                // --

                foreach (FOpcItem fOit in fChilds)
                {
                    FOpcDriverCommon.validateRemoveChildObject(this.fXmlNode, fOit.fXmlNode);
                }

                // --

                foreach (FOpcItem fOit in fChilds)
                {
                    fOit.remove();
                }
            }
            catch (Exception ex)
            {
                FDebug.throwException(ex);
            }
            finally
            {

            }
        }

