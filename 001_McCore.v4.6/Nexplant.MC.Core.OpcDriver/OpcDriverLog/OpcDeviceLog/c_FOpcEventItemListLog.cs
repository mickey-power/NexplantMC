        public FIObjectLog fParent
        {
            get
            {
                string logType = string.Empty;

                try
                {
                    if (this.fXmlNode.fParentNode == null)
                    {
                        return null;
                    }

                    // --

                    logType = this.fXmlNode.fParentNode.get_attrVal(FXmlTagOMGL.A_LogType, FXmlTagOMGL.D_LogType);
                    if (logType == FXmlTagOMGL.L_Read)
                    {
                        return new FOpcDeviceDataMessageReadLog(this.fOcdlCore, this.fXmlNode.fParentNode);
                    }
                    else if (logType == FXmlTagOMGL.L_Written)
                    {
                        return new FOpcDeviceDataMessageWrittenLog(this.fOcdlCore, this.fXmlNode.fParentNode);
                    }                   
                }
                catch (Exception ex)
